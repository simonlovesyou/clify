/**
 * Add two numbers
 * @param numberA - First number
 * @param numberB - Second number
 * @public
 */
export function add(numberA: number, numberB: number) {
  return numberA + numberB;
}

/**
 * Subtract two numbers
 * @description Second description
 * @param numberA - First number
 * @param numberB - Second number
 * @public
 */
export const subtract = (numberA: number, numberB: number) => {
  return numberA - numberB;
}
